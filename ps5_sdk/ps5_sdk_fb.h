/* ── Fill and rectangle ─────────────────────────────────────────────────── */

static void ps5sdk_fb_fill(u32 *fb, u32 w, u32 h, u32 color) {
    u32 n = w * h;
    for (u32 i = 0; i < n; i++) fb[i] = color;
}

static void ps5sdk_fb_rect(u32 *fb, u32 fb_w, u32 fb_h,
                            s32 x, s32 y, s32 w, s32 h, u32 color) {
    if (x < 0) { w += x; x = 0; }
    if (y < 0) { h += y; y = 0; }
    if (x + w > (s32)fb_w) w = (s32)fb_w - x;
    if (y + h > (s32)fb_h) h = (s32)fb_h - y;
    if (w <= 0 || h <= 0) return;
    for (s32 yy = 0; yy < h; yy++) {
        u32 *row = fb + (u32)(y + yy) * fb_w + (u32)x;
        for (s32 xx = 0; xx < w; xx++) row[xx] = color;
    }
}

/* ── Character and string rendering ────────────────────────────────────── */

/* Render one character. scale=1 → 8×8 px, scale=2 → 16×16 px, etc.
 * bg=0 with transparent=1 skips background pixels.               */
static void ps5sdk_fb_char(u32 *fb, u32 fb_w, u32 fb_h,
                            s32 x, s32 y, u8 ch,
                            u32 fg, u32 bg, u32 scale, u32 transparent_bg) {
    if (ch < 0x20 || ch > 0x7E) ch = '?';
    const u8 *g = ps5sdk_font8x8[ch - 0x20];
    for (u32 row = 0; row < 8; row++) {
        u8 bits = g[row];
        for (u32 col = 0; col < 8; col++) {
            u32 set = (bits >> col) & 1u;
            if (!set && transparent_bg) continue;
            u32 color = set ? fg : bg;
            ps5sdk_fb_rect(fb, fb_w, fb_h,
                           x + (s32)(col * scale),
                           y + (s32)(row * scale),
                           (s32)scale, (s32)scale, color);
        }
    }
}

/* Render a string. Supports '\n' for newline. Returns x after last char. */
static s32 ps5sdk_fb_str(u32 *fb, u32 fb_w, u32 fb_h,
                          s32 x, s32 y, const char *s,
                          u32 fg, u32 bg, u32 scale, u32 transparent_bg) {
    s32 cx = x, cy = y;
    s32 step = (s32)(8u * scale);
    while (*s) {
        if (*s == '\n') { cy += step; cx = x; }
        else {
            ps5sdk_fb_char(fb, fb_w, fb_h, cx, cy, (u8)*s,
                           fg, bg, scale, transparent_bg);
            cx += step;
        }
        s++;
    }
    return cx;
}

/* ── Numeric renderers ──────────────────────────────────────────────────── */

static s32 ps5sdk_fb_hex(u32 *fb, u32 fb_w, u32 fb_h,
                          s32 x, s32 y, u32 val, u32 digits,
                          u32 fg, u32 bg, u32 scale, u32 transparent_bg) {
    char buf[9];
    if (digits == 0 || digits > 8) digits = 8;
    for (u32 i = digits; i-- > 0;) {
        u32 nib = val & 0xFu;
        buf[i] = (char)(nib < 10u ? '0' + nib : 'A' + nib - 10u);
        val >>= 4;
    }
    buf[digits] = 0;
    return ps5sdk_fb_str(fb, fb_w, fb_h, x, y, buf, fg, bg, scale, transparent_bg);
}

static s32 ps5sdk_fb_dec(u32 *fb, u32 fb_w, u32 fb_h,
                          s32 x, s32 y, u32 val,
                          u32 fg, u32 bg, u32 scale, u32 transparent_bg) {
    char buf[11];
    u32 i = 10;
    buf[10] = 0;
    if (val == 0) { buf[--i] = '0'; }
    else { while (val && i > 0) { buf[--i] = '0' + (char)(val % 10u); val /= 10u; } }
    return ps5sdk_fb_str(fb, fb_w, fb_h, x, y, buf + i, fg, bg, scale, transparent_bg);
}

/* ── Printf-style renderer (requires ps5_sdk_runtime.h via ps5_sdk.h) ──── */

static void ps5sdk_fb_printf(u32 *fb, u32 fb_w, u32 fb_h,
                              s32 x, s32 y, u32 fg, u32 bg,
                              u32 scale, u32 transparent_bg,
                              const char *fmt, ...) {
    char tmp[128];
    __builtin_va_list ap;
    __builtin_va_start(ap, fmt);
    ps5_sdk_vsnprintf(tmp, (int)sizeof(tmp), fmt, ap);
    __builtin_va_end(ap);
    ps5sdk_fb_str(fb, fb_w, fb_h, x, y, tmp, fg, bg, scale, transparent_bg);
}

/* ── VideoOut takeover helper ───────────────────────────────────────────── */

static s32 ps5sdk_vo_open(void *G, void *vid_open) {
    s32 types[4] = { 0xFF, 0, 1, 2 };
    for (int i = 0; i < 4; i++) {
        s32 h = (s32)NC(G, vid_open, (u64)types[i], 0, 0, 0, 0, 0);
        if (h >= 0) return h;
    }
    return -1;
}

